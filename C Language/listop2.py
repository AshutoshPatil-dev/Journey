#Sorting
number = [30,20,60,10]
alpha = ["Hello", "Bye", "Yo"]

number.sort() #Sorts in ascending order by default
print(number)

number.sort(reverse=True)
print(number)

alpha.sort()
print(alpha)

alpha.sort(reverse = True)
print(alpha)

print(f"Max: {max(number)}")

print(f"Min: {min(number)}")

print(f"Sum: {sum(number)}")

